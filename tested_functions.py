def add(number1, number2):
    """That's a function that add two number"""
    result = number1 + number2
    return result


def divide(number1: int, number2: int) -> float:
    """That function make division between two given numbers.

    Args:
        number1: number to divide
        number2: divisor

    Returns: result of division

    """
    return number1/float(number2)
